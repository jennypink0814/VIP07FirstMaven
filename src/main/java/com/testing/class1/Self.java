package com.testing.class1;

public class Self {
    public static void main(String[] args) {
        int a=3,b=15;
        a++;
        System.out.println("后自增的结果："+a);
        ++a;
        System.out.println("前自增的结果："+a);
        b--;
        System.out.println("后自减的结果："+b);
        --b;
        System.out.println("前自减的结果："+b);

    }
}
